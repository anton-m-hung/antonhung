library(ggrepel)
volcano_plot <- function (dat, out_dir, out_name, title, fc_line, alpha, fc_label, alpha_label_no){
  ####

  #Description: Generates a volcano plot from standard results table output from DESeq2

  # Variables:
    # dat: (data frame) table output by results() from DESeq2
    # out_dir: (chara) path to output file name 
    # out_name: (chara) output file name
    # title: (chara) title to be shown on plot
    # fc_line: (numeric) position of x-axis at which to draw fold changes line (positive & negative)
    # aplha: (numeric) significance level to use for drawing line of y-axis (on -log10 scale)
    # fc_label: (numeric) minimum fold change value at which to begin labelling gene names
    # alpha_label_no: (numeric) minimum alpha value at which to begin labelling gene names 

  # Output: .png file of volcano plot 
  ###

  res_df <- as.data.frame(dat)
  res_df$sig <- c()
  res_df$cols <- c()

  for(i in 1:nrow(res_df)){
    if(is.na(res_df$padj[i])){
      res_df$sig[i] <- NA
      res_df$cols[i] <- NA
    }
    else if(res_df$padj[i]<=alpha & res_df$log2FoldChange[i] > fc_line){
      res_df$sig[i] <- paste0("Q-value <", alpha, ", Log2FC >", fc_line)
      res_df$cols[i] <- "indianred"
    } 
    else if(res_df$padj[i]<=alpha & res_df$log2FoldChange[i] < -fc_line){
      res_df$sig[i] <- paste0("Q-value <", alpha, ", Log2FC < -", fc_line)
      res_df$cols[i] <- "indianred"
    } 
    else if(res_df$padj[i]<=alpha & res_df$log2FoldChange[i]>-fc_line & res_df$log2FoldChange[i]<fc_line){
      res_df$sig[i] <- paste0("Q-value <", alpha, ", Log2FC > -", fc_line, "and ", ", Log2FC <", fc_line)
      res_df$cols[i] <- "cornflowerblue"
    } 
    else if(res_df$padj[i]>alpha & res_df$log2FoldChange[i] > fc_line){
      res_df$sig[i] <- paste0("Q-value >", alpha, ", Log2FC >", fc_line)
      res_df$cols[i] <- "gray47" 
    }
    else if(res_df$padj[i]>alpha & res_df$log2FoldChange[i] < -fc_line){
      res_df$sig[i] <- paste0("Q-value >", alpha, ", Log2FC < -", fc_line)
      res_df$cols[i] <- "gray47" 
    }
    else if(res_df$padj[i]>alpha & res_df$log2FoldChange[i] < fc_line){
      res_df$sig[i] <- paste0("Q-value >", alpha, ", Log2FC < -", fc_line)
      res_df$cols[i] <- "gray10" 
    }
  }
  
  # extract padj for nth gene (to use for labelling)
  if(any(res_df$padj <= 0.05)){
    res_small <- res_df[res_df$padj < 0.05, ]
    if(nrow(res_small) >= alpha_label_no){
      alpha_label <- res_df$padj[alpha_label_no]
    } else{
      alpha_label <- res_small$padj[nrow(res_small)]
    }
  } else{
      alpha_label <- 1
  }

  ppi=300
  png(paste0(out_dir, out_name), width=6*ppi, height=7*ppi, res=ppi)
  p = ggplot(res_df, aes(log2FoldChange, -log10(padj))) +
    geom_point(aes(col=sig),alpha = 0.5, shape = 21, size =2.5, colour = res_df$cols, fill = res_df$cols)  + 
    #xlim(-2.5, 2.5) +
    xlab("Log2 fold change estimate") + ylab("-log10(adj. P-value)") +
    geom_hline(yintercept = -log10(alpha), color = "black", linetype = "dashed", size = 0.4) + 
    labs(color="") + 
    theme(legend.key = element_blank()) + 
    ggtitle(title) + 
    geom_vline(xintercept = fc_line, colour = "black", linetype="dotted") + 
    geom_vline(xintercept = -fc_line, colour = "black", linetype="dotted") + 
      theme(legend.key.size = unit(1, "cm"), 
          #panel.grid.major = element_line(colour = "#d3d3d3"), 
          panel.border = element_rect(fill = NA, colour = "black", size = 0.6, linetype = "solid"), 
          panel.background = element_blank(),
          axis.text.x=element_text(colour="black", size = 12, hjust = 1), 
          axis.text.y=element_text(colour="black", size = 12), 
          axis.title.x=element_text(colour="black", size = 15), 
          axis.title.y=element_text(colour="black", size = 15), 
          legend.key = element_blank(), 
          legend.text = element_text(colour="black", size = 18), 
          legend.title = element_text(colour="black", size = 18)) + 
    # add labels to genes w/ LFC > 2 and above alpha threshold
    geom_label_repel(data = subset(res_df, log2FoldChange > fc_label & -log10(padj) >= 30), aes(label = gene), 
                     box.padding   = 0.35,
                     nudge_x = 0.01,
                     nudge_y = 0.01,
                     point.padding = 0.5,
                     label.size = 0.1,
                     segment.size = 0.3,
                     segment.color = 'grey50', size = 3) +
    # add labels to genes w/ LFC < -2 and above alpha threshold
    geom_label_repel(data = subset(res_df, log2FoldChange < -fc_label & -log10(padj) >= 30), aes(label = gene), 
                     box.padding   = 0.35,
                     nudge_x = -0.01,
                     nudge_y = 0.01,
                     point.padding = 0.5,
                     label.size = 0.1,
                     segment.size = 0.3,
                     segment.color = 'grey50', size = 3) +
    xlim(-10, 10)
    print(p)
    dev.off()
  
}
